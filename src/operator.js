
console.log('=, 대입 연산자');
var a = 1;
console.log(a);
console.log();

console.log('==, 동등 연산자');
console.log("1=='1': " + (1=='1'));
console.log();

console.log('===, 엄격한 동등 연산자');
console.log("1==='1'" + (1==='1'));
console.log();

// &&, AND 연산자
console.log('&&, AND 연산자')
console.log('true && true: ' + (true && true));
console.log('true && false: ' + (true && false));
console.log();

// ||, OR 연산자
console.log('||, OR 연산자')
console.log('true || true: ' + (true || true));
console.log('true || false: ' + (true || false));
console.log();

// !, 부정 연산자
console.log('!, 부정 연산자')
console.log('!(true && true): ' + !(true && true));
console.log('!(true || false): ' + !(true || false));
console.log();
